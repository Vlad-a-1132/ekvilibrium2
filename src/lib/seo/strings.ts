/** Короткий интро-текст для подкатегории (вариативность без копипаста). */
export function subcategorySeoParagraph(
  subName: string,
  mainName: string,
  mainSlug: string,
): string {
  const variants = [
    `«${subName}» в каталоге «${mainName}» — позиции для покупателей из Пятигорска и региона. Смотрите карточки товаров: там указаны наличие и цена; при необходимости уточним детали по телефону или почте.`,
    `Раздел «${subName}» внутри «${mainName}»: узкий ассортимент без лишнего. Заказ оформляется на сайте; для Пятигорска доступны согласованные способы получения. Если нужна помощь с выбором — напишите нам.`,
    `Ищете ${subName.toLowerCase()}? В этом подразделе «${mainName}» собраны подходящие позиции. «${SITE_BRAND}» работает в Пятигорске и помогает подобрать вариант под задачу — от бытовой покупки до подготовки к учебному сезону.`,
  ];
  const idx =
    (mainSlug.length + subName.length + (mainName.charCodeAt(0) ?? 0)) % variants.length;
  return variants[idx] ?? variants[0];
}
